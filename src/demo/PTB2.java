package demo;

public class PTB2 {
    private double a,b,c;
    public PTB2(){
    }
    public PTB2(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public void setA(double a) {
        this.a = a;
    }
    public double getA() {
        return this.a;
    }
    public void setB(double b) {
        this.b = b;
    }
    public double getB() {
        return this.b;
    }
    public void setC(double c) {
        this.c = c;
    }
    public double getC() {
        return this.c;
    }
}
